string = "belgrano"

def lastLetter(string):
        return string[-1]
    
print(lastLetter(string))


def firstLetter(string):
        return string[0]

print(firstLetter(string))


def randomLetter(string):
        return string[3]

print(randomLetter(string))


def rebanadaLetter(string):
        return string[0:3]

print(rebanadaLetter(string))